import java.util.List;

public class ListPrinter {
    public void printList(List<Integer> inputList) {
        // Logic để hiển thị danh sách
        // Đây chỉ là một đoạn mã mẫu
        for (Integer num : inputList) {
            System.out.print(num + " ");
        }
        System.out.println();
    }
}
